package basic;

public class DataTypesClass {

	public static void main(String[] args) {
		
		// DataTypes 
		// Primitive DataTypes : int, double, boolean and char
		// Non-Primitive DataTypes : String and Array
		
		// 1. int 
		int a = 10;    // int: datatype, a: variable, 10 value
		int b = 20;
		int m = -5;
		System.out.println("Value of A:" +a);
		System.out.println("Value of B:" +b);
		System.out.println("Adding A&B: " +(a+b));
		
		// 2. Double
		double d = 10.10;
		double d1 = 33;
		
		System.out.println(d1);
		
		// 3. char
		char c = 'A';
		char c1 = 'f';
		char c2 = '$';
		
		//4. boolean
		// In boolean true and false are values but its a java keywords. 
		boolean b2 = true;
		boolean b3 = false;
		
		
		// 5. String  - is a class, it is not a DataType but we can use as datatype.
		String s1 = "Vishal";
		String s2 = "1000";
		String s3 = "!@#$$%^";
		System.out.println(s3);
		

	}

}
